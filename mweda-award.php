  <div class="title" style="text-align:left !important;">MUSLIM WORLD ENTREPRENEUR DEVELOPMENT AWARD (MWEDA)</div>
        
<p>Because entrepreneurship has a significant contribution to the growth of local and international economies, which rely heavily on the progress made by small businesses that are established by ambitious entrepreneurs, the Muslim World Entrepreneurship Development Award (MWEDA) is presented to enhance this contribution by rewarding the best performers and encouraging other talented young men and women to follow their footsteps.</p>

<p>The MWEDA is honoured by OIC TODAY magazine and its first ceremony was held in Langkawi Island, Malaysia, in November 2018. Held at a prestigious resort in the marvelous Langkawi Island, the award ceremony will be attended by government officials, heads of diplomatic missions in Malaysia, renowned investors and business leaders in addition to esteemed media representatives.</p>

        
